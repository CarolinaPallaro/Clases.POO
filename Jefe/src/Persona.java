public class Persona {
    //Atributos
    private String nombre;
    private String apellido;
    private Integer dni;
    private Integer edad;

    //Constructor


    public Persona(String nombre, String apellido, Integer dni, Integer edad) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.dni = dni;
        this.edad = edad;
    }
}
